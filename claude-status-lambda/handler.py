# Fixed labels for the non-operational states (kept stable so the dashboard
# value-mappings can colour them yellow/red).
OUTAGE_LABELS = {
    "minor": "Minor Outage",
    "major": "Major Outage",
    "critical": "Critical Outage",
}


def _fetch_indicator():
    req = urllib.request.Request(STATUS_URL, headers={"Accept": "application/json"})
    with urllib.request.urlopen(req, timeout=8) as resp:
        data = json.loads(resp.read().decode("utf-8"))
    return (data.get("status") or {}).get("indicator", "unknown")


def handler(event, context):
    try:
        indicator = _fetch_indicator()
    except Exception:
        indicator = "unknown"

    if indicator == "none":
        display = random.choice(VERBS) + "..."
    elif indicator in OUTAGE_LABELS:
        display = OUTAGE_LABELS[indicator]
    else:
        display = "Status Unknown"

    body = [{"display": display, "indicator": indicator}]
    return {
        "statusCode": 200,
        "headers": {
            "Content-Type": "application/json",
            "Cache-Control": "no-store",
        },
        "body": json.dumps(body),
    }
